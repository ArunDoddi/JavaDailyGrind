package com.Udemy;

public class removingPunctuation {
    public static void main(String[] args) {
        System.out.println(Math.floor(Math.PI * 10.0)/10.0);
        System.out.println(Math.round(Math.PI * 100.0)/100.0);
        System.out.println(Math.round(Math.PI * 1000.0)/1000.0);
        System.out.println(Math.round(Math.PI * 10000.0)/10000.0);
        System.out.println(Math.round(Math.PI * 100000.0)/100000.0);
        System.out.println(Math.PI);
    }
}
